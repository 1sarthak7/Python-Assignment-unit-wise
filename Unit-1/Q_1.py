# Write a Python program to perform following operations on Lists:

# 1. Create and accesse list elements:
my_list = [10, 20, 30, 40, 50]
print("List elements:", my_list)

# 2. Add and Remove elements to a list:

my_list.append(60)                        # Adding an element
print("After adding an element:", my_list)

my_list.remove(20)                        # Removing an element
print("After removing an element:", my_list)

# 3. Sort and Reverse a list:

my_list.sort()                            # Sorting the list
print("Sorted list:", my_list)

my_list.reverse()                         # Reversing the list
print("Reversed list:", my_list)
